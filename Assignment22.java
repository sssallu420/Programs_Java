
//WAP to print value from 100 to -100 using for loop


public class Assignment22    //created class 
{
	    public static void main(String[] args)   //main method 
	    {
	        for (int i = 100; i >= -100; i--)   //for loop 
	        {
	            System.out.println(i);
	       
	        }
	    }	
}
