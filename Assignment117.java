

//WAP with continue keyworda?

public class Assignment117 {

	public static void main(String[] args) {

		int i = 20; // int decleration

		for (i = 0; i <= 20; i++) // for loop to print 0-5
		{

			if (i == 20) // if conditon which we want to skip
			{
				continue; // continue key word
			}
			System.out.println(i);

		}

	}

}
